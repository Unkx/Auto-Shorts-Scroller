// Detect language: full match first (e.g. "pt-BR"), then base (e.g. "pt")
function detectLang() {
  const langs = navigator.languages ?? [navigator.language ?? 'en'];
  for (const l of langs) {
    const full = l.toLowerCase().replace('-', '_');
    if (TRANSLATIONS[full]) return full;
    const base = l.split('-')[0].toLowerCase();
    if (TRANSLATIONS[base]) return base;
  }
  return 'en';
}

const LANG = detectLang();
const _t = TRANSLATIONS[LANG] ?? TRANSLATIONS.en;

// t('key') → translated string
function t(key, vars = {}) {
  let str = _t[key] ?? TRANSLATIONS.en[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replace(`{${k}}`, v);
  }
  return str;
}

// Apply all data-i18n / data-i18n-html attributes in the DOM
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml).replace(/\n/g, '<br>');
  });
}
